let sample = 12;

function firstFunc(sample)
{
    return sample + 4;
}
function secondFunc(sample)
{
    return sample - 4;
}
function thirdFunc(sample)
{
    return sample *4;
}
function fourthFunc(sample)
{
    return sample/4;
}
function fifthFunc(sample)
{
    return sample % 4; 
}

console.log(firstFunc(sample));
